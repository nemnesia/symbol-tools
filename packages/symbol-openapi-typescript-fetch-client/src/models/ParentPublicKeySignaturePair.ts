import { mapValues } from '../runtime.js';

/**
 *
 * @export
 * @interface ParentPublicKeySignaturePair
 */
export interface ParentPublicKeySignaturePair {
  /**
   * Public key.
   * @type {string}
   * @memberof ParentPublicKeySignaturePair
   */
  parentPublicKey: string;
  /**
   * Entity's signature generated by the signer.
   * @type {string}
   * @memberof ParentPublicKeySignaturePair
   */
  signature: string;
}

/**
 * Check if a given object implements the ParentPublicKeySignaturePair interface.
 */
export function instanceOfParentPublicKeySignaturePair(value: object): value is ParentPublicKeySignaturePair {
  if (!('parentPublicKey' in value) || value['parentPublicKey'] === undefined) return false;
  if (!('signature' in value) || value['signature'] === undefined) return false;
  return true;
}

export function ParentPublicKeySignaturePairFromJSON(json: any): ParentPublicKeySignaturePair {
  return ParentPublicKeySignaturePairFromJSONTyped(json, false);
}

export function ParentPublicKeySignaturePairFromJSONTyped(
  json: any,
  ignoreDiscriminator: boolean
): ParentPublicKeySignaturePair {
  if (json == null) {
    return json;
  }
  return {
    parentPublicKey: json['parentPublicKey'],
    signature: json['signature'],
  };
}

export function ParentPublicKeySignaturePairToJSON(json: any): ParentPublicKeySignaturePair {
  return ParentPublicKeySignaturePairToJSONTyped(json, false);
}

export function ParentPublicKeySignaturePairToJSONTyped(
  value?: ParentPublicKeySignaturePair | null,
  ignoreDiscriminator: boolean = false
): any {
  if (value == null) {
    return value;
  }

  return {
    parentPublicKey: value['parentPublicKey'],
    signature: value['signature'],
  };
}
